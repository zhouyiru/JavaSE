package com.code.sort;

public class run {

	private String name;
	int id;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public run(String name, int id) {
		super();
		this.name = name;
		this.id = id;
	}

}
